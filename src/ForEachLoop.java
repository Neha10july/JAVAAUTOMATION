
public class ForEachLoop {
    public static void main(String[] args) {

        String arr[] = {"Neha","Rahul","Sachin"};
        for(String str:arr){
            System.out.println(str);
        }
        int arr1[] = {1,2,3,4,5,6};
        for(int a:arr1){

            System.out.println(a);
        }
        System.out.println(arr1.length);
    }

}
